void
default_handler(int signum){
  cprintf("A signal %d was accepted by process %d \n ",signum, proc->pid);
}



sighandler_t signal(int signum, sighandler_t handler){
  if(signum<0 || signum >31){
    return (sighandler_t)-1;
  }
  sighandler_t previous_hendler = proc->signal_handlers[signum];
  proc->signal_handlers[signum]=handler;
  
  return previous_hendler;

}

int sigsend(int pid, int signum){

  struct proc *curr_proc=0;
  struct proc *p;

  if(signum<0 || signum >31){
    return -1;
  }

  acquire(&ptable.lock);
  //find the process to send signal to
  for(p = ptable.proc; p < &ptable.proc[NPROC] && curr_proc==0; p++){
    if(p->pid == pid)
        curr_proc=p;
  }

  if (curr_proc==0){
    release(&ptable.lock);
    return -1;
  }
  curr_proc->pending=(1<<signum)| curr_proc->pending;
  release(&ptable.lock);
  
  return 0;
}

void process_signal(struct trapframe* tf){

    uint ret_address;
    int length;
    uint esp_ptr;

    if (proc==0) //no process on this cpu
      return;
    
    if ((tf->cs&3)!= 0){ //check if trapframe of userspace process
      int sig= check_pending();

      if (sig==-1 ) //no signal to handle 
        return;

      if (proc->signal_handlers[sig]==default_handler){ //if default handler needs to be executed
        default_handler(sig);
      }
      else{ // handler is user space handler

        esp_ptr = proc->tf->esp; //esp_ptr will make all changes

        /*** copy sigret asm code to esp-    asm->backuptrapframe->signum->returnadress of asm code ***/
        length= (uint)&sigretend-(uint)&sigretstart;
        esp_ptr-=length; //keep space for the sigret func
        
        /*** set return adress to the start of sigret code ***/
        ret_address =esp_ptr; 
        memmove((void*)ret_address,sigretstart,length); 

        /*** add trapframe to user stack ***/
        esp_ptr-=sizeof(struct trapframe);
        memmove((void*)esp_ptr,proc->tf,sizeof(struct trapframe));

        /*** add signum to user stack ***/
        esp_ptr -=4; 
        *((int*)esp_ptr) = sig;

        /*** add return address to user stack ***/
        esp_ptr -=4;
        *((int*)esp_ptr) = ret_address;    

        /*** set eip to handler function ***/
        proc->tf->eip = (uint)proc->signal_handlers[sig]; 
        
        /*** restore esp pointer ***/
        proc->tf->esp = esp_ptr; 

      }
    }
}

int check_pending(){

  int signum=-1;
  acquire(&ptable.lock);
  if(proc->pending!=0){
    int i;
    int found=0;
    for(i=0;!found && i<NUMSIG;i++){
      if(((proc->pending>>i)&0x01)==1){
        found=1;
        signum=i;
        proc->pending &= ~(int)(1<<signum); //set current signal to 0
      }
    }
  }
  release(&ptable.lock);
  return signum;
}

int sigreturn(){

  struct trapframe* esp_tf = (struct trapframe*)(proc->tf->esp+4);
  proc->tf->edi = esp_tf->edi;
  proc->tf->esi = esp_tf->esi;
  proc->tf->ebp = esp_tf->ebp;
  proc->tf->oesp = esp_tf->oesp;
  proc->tf->ebx = esp_tf->ebx;
  proc->tf->edx = esp_tf->edx;
  proc->tf->ecx = esp_tf->ecx;
  proc->tf->eax = esp_tf->eax;
  proc->tf->eip = esp_tf->eip;
  proc->tf->eflags = esp_tf->eflags;
  proc->tf->esp = esp_tf->esp;
  //memmove((void*)proc->tf,(void*)(proc->tf->esp+4),sizeof(struct trapframe));
  return 0;
}

int alarm(int alarm_time){
  if (alarm_time==0){
    proc->alarm_time=-1;
    return 0;
  }
  else{
    proc->alarm_time=alarm_time;
  }
  return 1;
}


void interrupt_alarm(uint timex){
  struct proc *p;
  for(p = ptable.proc; p < &ptable.proc[NPROC]; p++){
    if (p->alarm_time>timex || p->alarm_time==-1)
      continue;
    else{
      p->alarm_time=-1;
      sigsend(p->pid,SIGALRM);
    }
  }
}